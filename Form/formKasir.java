    protected void datatable() {
        Object[] Baris ={"ID Kasir", "Nama Kasir", "Jenis Kelamin", "No. Telepon", "Agama", "Alamat", "Password"};
            tabmode = new DefaultTableModel(null, Baris); //tablebarang.setModel(tabmode);
            String cariitem = txtcari.getText();
            
            try {
                String sql = "SELECT * FROM kasir where idkasir like '%"+cariitem+"%' or nmkasir like '%"+cariitem+"%' order by idkasir asc";
                Statement stat = conn.createStatement();
                ResultSet hasil = stat.executeQuery(sql);
                while (hasil.next()) {
                    tabmode.addRow(new Object[] {
                       hasil.getString(1),
                       hasil.getString(2),
                       hasil.getString(3),
                       hasil.getString(4),
                       hasil.getString(5),
                       hasil.getString(6),
                       hasil.getString(7),
                });
             }
                tblkasir.setModel(tabmode);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data Kasir gagal dipanggil"+e);
            }
        }

