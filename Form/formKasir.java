    private void btsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsimpanActionPerformed
        String jenkel = null;
        if(rlaki.isSelected()) {
            jenkel = "Laki-Laki";
        } else if(rperempuan.isSelected()) {
            jenkel = "Perempuan";
        }
        String sql = "INSERT INTO kasir (idkasir, nmkasir, jenkel, notelp, agama, almt, pass) VALUES (?, ?, ?, ?, ?, ?, ?)";

        
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtid.getText());
            stat.setString(2, txtnmkasir.getText());
            stat.setString(3, jenkel);
            stat.setString(4, txttelp.getText());
            stat.setString(5, txtagama.getText());
            stat.setString(6, txtalmt.getText());
            stat.setString(7, txtpass.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Kasir berhasil disimpan");
            kosong();
            txtid.requestFocus();
        }
        catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Kasir gagal disimpan"+e);
        }
        datatable();
    }//GEN-LAST:event_btsimpanActionPerformed

